        private void client_DownloadStringCompleted(object sender, DownloadStringCompletedEventArgs e)
        {
       
            Newtonsoft.Json.Linq.JArray jObject = Newtonsoft.Json.Linq.JArray.Parse(e.Result);
            foreach (string item in jObject)
            {
                string[] split = null;
                split = item.ToString().Split();
                
                WebClient client = new WebClient();
                Uri uri = new Uri("http://iam.colum.edu/portfolio/api/course/" + split[0] + "?json=True", UriKind.Absolute);  //set the URI and URIKind
                
                //get using strings and async call
                client.DownloadStringCompleted += new DownloadStringCompletedEventHandler(client_DownloadStringCompleted2);
                client.DownloadStringAsync(uri);
            }

        }

        private void client_DownloadStringCompleted2(object sender, DownloadStringCompletedEventArgs e)
